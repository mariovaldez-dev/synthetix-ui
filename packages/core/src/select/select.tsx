import * as React from "react";
import * as SelectPrimitive from "@radix-ui/react-select";

import { cn } from "@synthetix-ui/utils";
import type {
  SelectProps,
  SelectGroupProps,
  SelectValueProps,
  SelectTriggerProps,
  SelectContentProps,
  SelectLabelProps,
  SelectItemProps,
  SelectSeparatorProps,
  SelectScrollUpButtonProps,
  SelectScrollDownButtonProps,
} from "./select.types";

export function Select({
  options,
  label,
  placeholder,
  helperText,
  errorText,
  className,
  children,
  ...props
}: SelectProps) {
  const selectContent = options ? (
    <>
      <SelectTrigger className={className}>
        <SelectValue placeholder={placeholder} />
      </SelectTrigger>
      <SelectContent>
        <SelectPrimitive.Group>
          {label && (
            <SelectPrimitive.Label className={cn("py-1.5 pl-8 pr-2 text-sm font-semibold")}>
              {label}
            </SelectPrimitive.Label>
          )}
          {options.map((opt) => (
            <SelectItem 
              key={opt.value} 
              value={opt.value} 
              {...(opt.disabled !== undefined ? { disabled: opt.disabled } : {})}
            >
              {opt.label}
            </SelectItem>
          ))}
        </SelectPrimitive.Group>
      </SelectContent>
    </>
  ) : children;

  const inner = (
    <SelectPrimitive.Root {...props}>
      {selectContent}
    </SelectPrimitive.Root>
  );

  if (options || helperText || errorText) {
    return (
      <div className="flex flex-col gap-1.5">
        {(label && !options) && <div className="text-sm font-medium leading-none text-foreground">{label}</div>}
        {inner}
        {helperText && !errorText && (
          <p className="text-xs text-muted-foreground">{helperText}</p>
        )}
        {errorText && (
          <p className="text-xs text-destructive font-medium">{errorText}</p>
        )}
      </div>
    );
  }

  return inner;
}

export const SelectGroup = SelectPrimitive.Group;
export const SelectValue = SelectPrimitive.Value;

export const SelectTrigger = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Trigger>,
  SelectTriggerProps
>(({ className, children, ...props }, ref) => (
  <SelectPrimitive.Trigger
    ref={ref}
    className={cn(
      "flex h-10 w-full items-center justify-between rounded-md border border-input bg-background px-3 py-2 text-sm ring-offset-background placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2 disabled:cursor-not-allowed disabled:opacity-50 [&>span]:line-clamp-1",
      className
    )}
    {...props}
  >
    {children}
    <SelectPrimitive.Icon asChild>
      <svg
        xmlns="http://www.w3.org/2000/svg"
        width="24"
        height="24"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
        className="h-4 w-4 opacity-50"
      >
        <path d="m6 9 6 6 6-6" />
      </svg>
    </SelectPrimitive.Icon>
  </SelectPrimitive.Trigger>
));
SelectTrigger.displayName = SelectPrimitive.Trigger.displayName;

